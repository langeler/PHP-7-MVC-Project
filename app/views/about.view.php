<h2>
    Historien bakom Mama Bear Café...

</h2>

<p>
Mama Bear är en gammal dröm som legat på is i många år. Jag har arbetat som HR Direktör på ett stort företag inne i stan. Men någonting hände den 27 juli 2016, vår yngsta son blev väldigt sjuk. Jag var hemma med honom i 8 månader och han missade nästan hela förskoleklassen. Därefter tänkte vi att jag behöver finnas i närheten när han är dålig och varför inte ett café i Ursvik där vi också bor.. men det var svårt med lokaler och byggherrar- svarta kontrakt och en massa andra jobbiga saker.
</p>>

<p>
Därefter fick vi behandling IVIG en fantastisk behandling som gav familjen och främst yngsta sonen livet tillbaka. Och jag kunde åter jobba full tid igen med HR och arbetsrätt.
</p>

<p>
Men denna behandling drogs utan förvarning och information in i november 2017 och sedan dess har han blivit sämre. Då kom idén upp igen att starta ett café. Tillslut fick vi tag i en lokal och det är nu Ursviks allé 27 - denna renoverar vi nu för att få ett mysigt café en möjlighet för mig att vara i närheten av hemmet för sonens skull men även för att det är något jag brinner för att baka- hemlagat. Vi planerar att öppna den under juli om allt går som det ska:)
</p>

<p>
Vi kommer figurera en del i media pga. sonens sjukdom och den avbrutna behandlingen så bli inte förvånade om ni ser mitt namn här och där - senast var vi med i Aftonbladet.
</p>

<p>
Jag hoppas inte bara att detta café ska skänka mig ett lugn att kunna vara i närheten av sonen utan också ge er alla som bor här i Ursvik ett mysigt café med gott kaffe, hembakade bakverk, bröd och glass såklart.
</p>

